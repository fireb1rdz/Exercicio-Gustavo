def adicao(n1: int, n2: int) -> int:
    """Realiza a soma de dois números

    Args:
        n1: primeiro número.
        n2: segundo número.

    Returns:
        A soma dos números.
    """
    return n1 + n2


def subtracao(n1: int, n2:int) -> int:
    """Realiza a subtração de dois números

    Args:
        n1: primeiro número.
        n2: segundo número.

    Returns:
        A subtração dos números.
    """
    return n1 - n2

def divisao(n1: float, n2: float) -> float:
    """Realiza a divisão de dois números

    Args:
        n1: primeiro número.
        n2: segundo número.

    Returns:
        O quociente dos números.
    """
    return n1 / n2


def multiplicacao(n1: int, n2: int) -> int:
    """Realiza a multiplicação de dois números

        Args:
            n1: primeiro número.
            n2: segundo número.

        Returns:
            O produto dos números.
        """
    return n1 * n2 * 2
